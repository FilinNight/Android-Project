package com.lingvolern.Presenter;

public class User
{
    private String name;
    private int level;

    public User ()
    {
        level = 1;
    }



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }



    
}
